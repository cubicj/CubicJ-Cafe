    SELECT old.value
    FROM system_settings old
    JOIN ltx_lora_key_migration keys ON keys.old_key = old.key
    WHERE keys.new_key = system_settings.key
  ),
  type = (
    SELECT old.type
    FROM system_settings old
    JOIN ltx_lora_key_migration keys ON keys.old_key = old.key
    WHERE keys.new_key = system_settings.key
  ),
  category = 'ltx',
  updated_at = datetime('now')
WHERE key IN (SELECT new_key FROM ltx_lora_key_migration)
  AND EXISTS (
    SELECT 1
    FROM system_settings old
    JOIN ltx_lora_key_migration keys ON keys.old_key = old.key
    WHERE keys.new_key = system_settings.key
  );

